const dicts: Record<Lang, Dict> = { en, es };

interface I18n {
  lang: Lang;
  setLang: (l: Lang) => void;
  toggle: () => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18n>({ lang: 'en', setLang: () => {}, toggle: () => {}, t: (k) => k });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('nw_lang') : null;
    return saved === 'es' ? 'es' : 'en';
  });
  useEffect(() => { document.documentElement.lang = lang; }, [lang]);
  const setLang = (l: Lang) => { setLangState(l); try { localStorage.setItem('nw_lang', l); } catch {} };
  const toggle = () => setLang(lang === 'en' ? 'es' : 'en');
  const t = (key: string) => dicts[lang][key] ?? dicts.en[key] ?? key;
  return <I18nContext.Provider value={{ lang, setLang, toggle, t }}>{children}</I18nContext.Provider>;
}

export const useT = () => useContext(I18nContext);

/** Small EN/ES pill toggle. */
export function LangToggle({ className = '' }: { className?: string }) {
  const { lang, setLang } = useT();
  return (
    <div className={`flex items-center bg-gray-100 rounded-full p-1 ${className}`}>
      <button onClick={() => setLang('en')}
        className={`px-3 py-1 rounded-full text-xs font-bold transition-colors ${lang === 'en' ? 'bg-white text-gray-800 shadow-sm' : 'text-gray-500 hover:text-gray-800'}`}>EN</button>
      <button onClick={() => setLang('es')}
        className={`px-3 py-1 rounded-full text-xs font-bold transition-colors ${lang === 'es' ? 'bg-white text-gray-800 shadow-sm' : 'text-gray-500 hover:text-gray-800'}`}>ES</button>
    </div>
  );
}
